#!/usr/bin/env node
/**
 * generate-color-scales.mjs
 *
 * Generate OKLCH-based Tailwind v4 color scales (50-950) for Arc's Primitive layer.
 *
 * Anchors come from `docs/design/design.md` (HeroUI Pro Theme Builder output).
 * For each palette, the anchor color sits at a specific stop based on its lightness;
 * other stops are derived by adjusting L (lightness) and tapering C (chroma).
 *
 * Usage:
 *   node tools/generate-color-scales.mjs              # print all scales as CSS
 *   node tools/generate-color-scales.mjs --json       # print as JSON
 *   node tools/generate-color-scales.mjs --hex        # also print hex equivalents
 *
 * See docs/adr/003-design-tokens.md §v3.1 + docs/adr/005-color-scale-system.md.
 */

import { formatHex, oklch } from "culori";

// ─── Palette definitions (anchor + L/C distribution) ──────────────────────

const STOPS = [50, 100, 200, 300, 400, 500, 600, 700, 800, 900, 950];

/**
 * L (lightness) profile shared across all palettes.
 * Mirrors Tailwind v4's default neutral scale. anchor stop's L gets overridden
 * by the actual anchor value below to preserve color identity.
 */
const L_PROFILE = {
  50: 0.98,
  100: 0.96,
  200: 0.92,
  300: 0.86,
  400: 0.78,
  500: 0.68,
  600: 0.58,
  700: 0.46,
  800: 0.36,
  900: 0.24,
  950: 0.16,
};

/**
 * Per-stop C (chroma) attenuation factor (0-1).
 * 1.0 = full anchor chroma; lower = more desaturated.
 * Bell-shaped: peaks near anchor stop, tapers at extremes (50 / 950)
 * to avoid neon-pastel and muddy-deep artifacts.
 */
const C_PROFILE = {
  50: 0.18,
  100: 0.34,
  200: 0.62,
  300: 0.92,
  400: 1.0,
  500: 0.95,
  600: 0.85,
  700: 0.7,
  800: 0.55,
  900: 0.38,
  950: 0.24,
};

/**
 * Palettes with anchors from design.md.
 * `anchorStop` = the stop where the anchor color "naturally" sits based on its L.
 * Other stops are derived; the anchor stop preserves the exact OKLCH from design.
 */
const PALETTES = {
  // Brand: HeroUI accent (#50FF6C, oklch(88.15% 0.2375 145.76))
  // Anchor at 300 (L=88% sits between 200=92% and 300=86%, but visually neon green
  // belongs in the lighter half).
  brand: { L: 0.8815, C: 0.2375, H: 145.76, anchorStop: 300 },

  // Green: HeroUI success (#64C33A, oklch(73.29% 0.1942 137.87))
  // Anchor at 500 (L=73% near typical 500 position).
  green: { L: 0.7329, C: 0.1942, H: 137.87, anchorStop: 500 },

  // Red: HeroUI danger (#FD3367, oklch(65.32% 0.2337 12.76))
  // Anchor at 500.
  red: { L: 0.6532, C: 0.2337, H: 12.76, anchorStop: 500 },

  // Orange: HeroUI warning (#FF9C3E, oklch(78.19% 0.1590 59.36))
  // Anchor at 400 (L=78% closest to 400=78%).
  orange: { L: 0.7819, C: 0.159, H: 59.36, anchorStop: 400 },

  // Blue: Arc info token (proposed HeroUI default blue #0485F7,
  // oklch(62.04% 0.195 253.83))
  // Anchor at 500.
  blue: { L: 0.6204, C: 0.195, H: 253.83, anchorStop: 500 },

  // Neutral: gray scale; near-zero chroma, slight cool tint (H=285.89 from eclipse).
  // No "anchor" — the scale is the value. Endpoints chosen to match snow / eclipse.
  neutral: { L: 0.5, C: 0.005, H: 285.89, anchorStop: null },
};

// ─── Generation ────────────────────────────────────────────────────────────

/**
 * Format a number as percentage string with reasonable precision.
 * 0.8815 -> "88.15%"
 */
function pct(n, decimals = 2) {
  return `${(n * 100).toFixed(decimals)}%`;
}

/**
 * Format chroma with adaptive precision (small values get more decimals).
 */
function chromaStr(c) {
  if (c < 0.01) return c.toFixed(4);
  if (c < 0.1) return c.toFixed(3);
  return c.toFixed(3);
}

function buildOklchString(L, C, H) {
  // Special case for pure neutrals (C ≈ 0): omit hue for clarity.
  if (C < 0.001) {
    return `oklch(${pct(L)} 0 0)`;
  }
  return `oklch(${pct(L)} ${chromaStr(C)} ${H.toFixed(2)})`;
}

function generateScale(palette) {
  const { L: anchorL, C: anchorC, H, anchorStop } = palette;
  const result = {};

  for (const stop of STOPS) {
    let L = L_PROFILE[stop];
    let C;

    if (anchorStop === stop) {
      // Preserve exact anchor at its natural stop.
      L = anchorL;
      C = anchorC;
    } else {
      // Derive C: scale anchor's max chroma by the per-stop profile.
      C = anchorC * C_PROFILE[stop];
    }

    result[stop] = {
      L,
      C,
      H,
      oklch: buildOklchString(L, C, H),
      hex: formatHex(oklch({ mode: "oklch", l: L, c: C, h: H })) || "n/a",
    };
  }

  return result;
}

// ─── Main output ──────────────────────────────────────────────────────────

function emitCss() {
  let css = "/*\n";
  css += " * Arc Primitive Color Scales — Tailwind v4 OKLCH\n";
  css += " * Generated by tools/generate-color-scales.mjs\n";
  css += " * See docs/adr/003-design-tokens.md §v3.1\n";
  css += " *\n";
  css += " * DO NOT EDIT MANUALLY — re-run the script and copy output.\n";
  css += " */\n\n";

  for (const [name, palette] of Object.entries(PALETTES)) {
    const scale = generateScale(palette);
    css += `  /* ${name.charAt(0).toUpperCase()}${name.slice(1)} */\n`;
    for (const stop of STOPS) {
      const { oklch, hex } = scale[stop];
      const marker = palette.anchorStop === stop ? "  /* ← anchor */" : "";
      css += `  --color-${name}-${stop}: ${oklch};${marker}  /* ${hex} */\n`;
    }
    css += "\n";
  }

  return css;
}

function emitJson() {
  const out = {};
  for (const [name, palette] of Object.entries(PALETTES)) {
    const scale = generateScale(palette);
    out[name] = {};
    for (const stop of STOPS) {
      out[name][stop] = scale[stop];
    }
  }
  return JSON.stringify(out, null, 2);
}

// ─── CLI ──────────────────────────────────────────────────────────────────

const args = process.argv.slice(2);
if (args.includes("--json")) {
  console.log(emitJson());
} else {
  console.log(emitCss());
}
